package basics;

public class ArithimaticOperators 
{
	static int a=10;
	static int b=20;
	
	static void sab() 
	{
		System.out.println(a+b);
	}
	
	static void minu() 
	{
		System.out.println(a-b);
	}
	static void mul() 
	{
		System.out.println(a*b);
	}
	static void div() 
	{
		System.out.println(b/a);
	}
	

	public static void main(String[] args) 
	{
		sab();
		minu();
		mul();
		div();

	}

}
